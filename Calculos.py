def suma():
    print(f'Sumar')
    num1 = float(input('Introduce el primer valor: '))
    num2 = float(input('Introduce el segundo valor: '))
    print(f'La suma es: {num1+num2}')

def resta():
    print(f'Restar')
    num1 = float(input('Introduce el primer valor: '))
    num2 = float(input('Introduce el segundo valor: '))
    print(f'La resta es: {num1 - num2}')



def division():
    print('Dividir')
    num1 = float(input('Introduce el primer valor: '))
    num2 = float(input('Introduce el segundo valor: '))
    print(f'La division es: {num1/num2}')
